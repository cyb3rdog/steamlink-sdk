#!/bin/bash
#

TOP=$(cd `dirname "${BASH_SOURCE[0]}"` && pwd)
if [ "${MARVELL_SDK_PATH}" = "" ]; then
	MARVELL_SDK_PATH="$(cd "${TOP}/../.." && pwd)"
fi
if [ "${MARVELL_ROOTFS}" = "" ]; then
	source "${MARVELL_SDK_PATH}/setenv.sh" || exit 1
fi
BUILD="${PWD}"
SRC="${BUILD}/prboom-plus-src"

#
# Download the source
#
if [ ! -d "${SRC}" ]; then
	git clone https://github.com/superna9999/prboom-plus.git "${SRC}"
	rm -f "${BUILD}/.patch-applied"
fi

#
# Apply any patches
#
if [ "${TOP}/prboom-plus.patch" -nt "${BUILD}/.patch-applied" ]; then
	pushd "${SRC}"
	git reset
	git clean -fxd
	git checkout .
	patch -p1 <"${TOP}/prboom-plus.patch" || exit 1
	git add .
	cp "${MARVELL_ROOTFS}/usr/share/aclocal/sdl2.m4" autotools/
	./bootstrap
	popd
	touch "${BUILD}/.patch-applied"
fi

#
# Build it
#
pushd "${SRC}"
./configure --host=$SOC_BUILD --prefix=/home/apps/prboom-plus --disable-gl || exit 1
make install DESTDIR="${BUILD}/tmp" || exit 1
popd

#
# Get the data file
#
WAD=shareware_doom_iwad.zip
if [ ! -f "${WAD}" ]; then
	wget http://www.doomworld.com/3ddownloads/ports/${WAD}
fi

MIDI=dgguspat.zip
if [ ! -f "${MIDI}" ]; then
	wget http://ftp.mancubus.net/pub/idgames/music/${MIDI}
fi


export APPSDIR="${BUILD}/steamlink/apps"
export DESTDIR="${APPSDIR}/prboom-plus"

# Copy the files to the app directory
mkdir -p "${APPSDIR}"
rm -rf "${DESTDIR}"
mv -v "${BUILD}/tmp/home/apps/prboom-plus" "${DESTDIR}" || exit 1
$STRIP "${DESTDIR}/games/prboom-plus"
pushd "${DESTDIR}/share/games/doom" || exit 1
unzip "${BUILD}/${WAD}"
for file in *.WAD
do mv -v $file $(echo $file | tr '[A-Z]' '[a-z]')
done
popd
pushd "${DESTDIR}"
mkdir timidity
cd timidity || exit 1
unzip "${BUILD}/${MIDI}"
echo "dir /home/apps/prboom-plus/timidity" >timidity.cfg.new
cat timidity.cfg >>timidity.cfg.new
mv timidity.cfg.new timidity.cfg
popd
rm -rf "${BUILD}/tmp"


# Create the table of contents and icon
cat >"${DESTDIR}/run.sh" <<__EOF__
#!/bin/sh
#
export TIMIDITY_CFG=timidity/timidity.cfg
export DOOMWADDIR=$(dirname $0)/share/games/doom

games/prboom-plus -width 1280 -height 720
__EOF__
chmod 755 "${DESTDIR}/run.sh"

cat >"${DESTDIR}/toc.txt" <<__EOF__
name=PrBoom
icon=icon.png
run=run.sh
__EOF__

